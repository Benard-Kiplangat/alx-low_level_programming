#include "3-calc.h"

/**
 * op_add - a function that sums two integers
 * 
 * @a: integer a
 * @b: integer b
 *
 * Return: the sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - a function that subtracts two integers
 *
 * @a: integer a
 * @b: integer b
 * 
 * Return: subtraction of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - a function that multiplies two integers
 *
 * @a: integer a
 * @b: integer b
 * 
 * Return: multiplication of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - a function that divides two integers
 *
 * @a: integer a
 * @b: integer b
 * 
 * Return: division of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a function that divides two integers
 *
 * @a: integer a
 * @b: integer b
 *
 * Return: modulus of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
